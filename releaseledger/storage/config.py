"""Project-local TOML configuration for releaseledger (version 2).

This module owns parsing, validating, and rendering the canonical
``.ledger/releaseledger/config.toml`` file. Storage topology is not
configured here; the canonical project manifest owns that. The legacy
config version 1 fields (``releaseledger_dir``, ``releaseledger_dir_policy``,
``ledger_next_entry_number``, ``ledger.name``) are not recognised.

Path resolution relative to the config file lives in
:mod:`releaseledger.storage.paths`; this module deliberately has no
filesystem discovery so it can be unit-tested against plain dicts and
strings.
"""

