def _config_to_tomlkit(config: ProjectConfig) -> tomlkit.TOMLDocument:
    """Render a :class:`ProjectConfig` to a fresh :mod:`tomlkit` document.

    Comments are intentionally omitted because the rendered output is the
    canonical default; the writer is paired with the patcher for round-trip
    updates, and :func:`write_project_config` keeps existing comments when
    the file is already on disk.
    """

    doc = tomlkit.document()
    doc.add("config_version", config.config_version)
    doc.add(tomlkit.comment("Branch-scoped release state. This block is safe to commit."))
    doc.add("ledger_ref", config.ledger_ref)
    doc.add("ledger_parent_ref", config.ledger_parent_ref)
    doc.add("ledger_branch_guard", config.ledger_branch_guard)
    doc.add(tomlkit.comment("Cross-ledger identity. Local record IDs stay unchanged."))
    ledger = tomlkit.table()
    ledger.add("code", config.ledger_code)
    doc.add("ledger", ledger)
