def write_project_config(
    config_path: Path,
    config: ProjectConfig,
    *,
    preserve_comments: bool = True,
) -> None:
    """Atomically write ``config`` to ``config_path``.

    When ``preserve_comments`` is true and ``config_path`` already exists,
    the existing TOML document is read with :mod:`tomlkit`, the canonical
    v2 fields are updated in place, and removed v1 fields are stripped
    so the file conforms to the new schema. The original file is left
    unchanged on parse/validation/write failure because the write goes
    through :func:`ledgercore.atomic_write_text`.
    """

    if config.config_version != CONFIG_VERSION:
        raise LaunchError(
            "Refusing to write a config whose config_version is not 2.",
            code=CODE_CONFIG_ERROR,
            exit_code=2,
            data={"config_version": config.config_version, "required": CONFIG_VERSION},
        )

    if preserve_comments and config_path.is_file():
        try:
            text = config_path.read_text(encoding="utf-8")
            document = tomlkit.parse(text)
        except tomlkit.exceptions.TomlKitError as exc:
            raise LaunchError(
                f"Failed to parse {config_path.name}: {exc}",
                code=CODE_CONFIG_ERROR,
                exit_code=2,
                data={"path": str(config_path)},
            ) from exc
        _apply_config_to_document(document, config)
    else:
        document = _config_to_tomlkit(config)

    rendered = tomlkit.dumps(document)
    ledgercore.atomic_write_text(config_path, rendered)


def _apply_config_to_document(
    document: tomlkit.TOMLDocument, config: ProjectConfig
) -> None:
    """Mutate ``document`` so it matches ``config`` while preserving comments.

    Unknown v1 keys (``releaseledger_dir``, ``releaseledger_dir_policy``,
    ``ledger_next_entry_number``, ``ledger.name``) are removed. New
    sections are created when missing. Existing keys keep their
    surrounding comments and order where the new schema allows it.
    """

    def _set(table: Any, key: str, value: Any) -> None:
        if key in table:
            table[key] = value
        else:
            table.add(key, value)

    _set(document, "config_version", config.config_version)
    _set(document, "ledger_ref", config.ledger_ref)
    _set(document, "ledger_parent_ref", config.ledger_parent_ref)
    _set(document, "ledger_branch_guard", config.ledger_branch_guard)
    for removed in REMOVED_FIELDS:
        if removed in document:
            del document[removed]
    ledger = document.get("ledger")
    if ledger is None:
        ledger = tomlkit.table()
        document.add("ledger", ledger)
    _set(ledger, "code", config.ledger_code)
    if "name" in ledger:
        del ledger["name"]
    release = document.get("release")
    if release is None:
        release = tomlkit.table()
        document.add("release", release)
    _set(release, "default_changelog", config.default_changelog)
    _set(release, "default_status", config.default_status)
    _set(release, "allow_dirty_worktree", config.allow_dirty_worktree)
    changelog = document.get("changelog")
    if changelog is None:
        changelog = tomlkit.table()
        document.add("changelog", changelog)
    _set(changelog, "output", config.changelog_output)
    _set(changelog, "header", config.changelog_header)
    _set(changelog, "body", tomlkit.string(config.changelog_body, multiline=True))
    _set(changelog, "footer", config.changelog_footer)
    _set(changelog, "trim", config.changelog_trim)
    _set(changelog, "render_always", config.changelog_render_always)
    _set(changelog, "postprocessors", list(config.changelog_postprocessors))
    _set(changelog, "standard", config.changelog_standard)
    _set(changelog, "group_mode", config.changelog_group_mode)
    _set(changelog, "link_references", config.changelog_link_references)
    _set(changelog, "repository_url", config.changelog_repository_url)
    _set(changelog, "tag_prefix", config.changelog_tag_prefix)
    _set(changelog, "compare_url_template", config.changelog_compare_url_template)
    _set(changelog, "release_url_template", config.changelog_release_url_template)
    _set(changelog, "preamble", config.changelog_preamble)
    _set(changelog, "semver", config.changelog_semver)
    _set(changelog, "unreleased", config.changelog_unreleased)
    if config.changelog_templates is not None:
        _set(changelog, "templates", config.changelog_templates)
    elif "templates" in changelog:
        del changelog["templates"]
    git = document.get("git")
    if git is None:
        git = tomlkit.table()
        document.add("git", git)
    _set(git, "enabled", config.git_enabled)
    _set(git, "default_base", config.git_default_base)
    _set(git, "default_head", config.git_default_head)
    _set(git, "previous_tag_patterns", list(config.git_previous_tag_patterns))
    _set(git, "include_merges", config.git_include_merges)
    _set(git, "require_clean_worktree", config.git_require_clean_worktree)
    _set(git, "max_commits", config.git_max_commits)
    _set(git, "max_diff_chars_per_commit", config.git_max_diff_chars_per_commit)
    _set(git, "candidate_status", config.git_candidate_status)


def update_project_config(
    config_path: Path,
    changes: Mapping[str, Any],
) -> ProjectConfig:
    """Read, apply ``changes``, validate, and atomically write the config.

    Returns the resulting :class:`ProjectConfig`. ``changes`` keys may
    be dotted (``"ledger_ref"``, ``"changelog.body"``, ``"git.max_commits"``)
    or a mapping for nested sections. Unknown keys raise a structured
    error. The original file is unchanged on parse/validation/write
    failure because the write goes through
    :func:`ledgercore.atomic_write_text`.
    """

    if not config_path.is_file():
        raise LaunchError(
            f"Project config not found: {config_path}",
            code=CODE_CONFIG_ERROR,
            exit_code=2,
            data={"path": str(config_path)},
            remediation=[
                "Run `releaseledger init` to create the config.",
            ],
        )
    config = load_project_config(config_path)
    updated = _apply_changes(config, changes)
    write_project_config(config_path, updated)
    return updated


def _apply_changes(
    config: ProjectConfig, changes: Mapping[str, Any]
) -> ProjectConfig:
    """Translate a dotted-key patch into a typed config update."""

    flat: dict[str, Any] = {}
    for key, value in changes.items():
        if isinstance(value, Mapping) and key in {"ledger", "release", "changelog", "git"}:
            for sub_key, sub_value in value.items():
                flat[f"{key}.{sub_key}"] = sub_value
        else:
            flat[key] = value

    if "ledger_ref" in flat:
        config = config.replace(ledger_ref=_require_str(flat["ledger_ref"], "ledger_ref", "patch"))
    if "ledger_parent_ref" in flat:
        config = config.replace(
            ledger_parent_ref=_require_str(flat["ledger_parent_ref"], "ledger_parent_ref", "patch")
        )
    if "ledger_branch_guard" in flat:
        config = config.replace(
            ledger_branch_guard=_require_str(flat["ledger_branch_guard"], "ledger_branch_guard", "patch")
        )
    if "ledger.code" in flat:
        config = config.replace(ledger_code=_require_str(flat["ledger.code"], "ledger.code", "patch"))
    if "release.default_changelog" in flat:
        config = config.replace(
            default_changelog=_require_str(
                flat["release.default_changelog"], "release.default_changelog", "patch"
            )
        )
    if "release.default_status" in flat:
        config = config.replace(
            default_status=_require_str(
                flat["release.default_status"], "release.default_status", "patch"
            )
        )
    if "release.allow_dirty_worktree" in flat:
        config = config.replace(
            allow_dirty_worktree=_require_bool(
                flat["release.allow_dirty_worktree"], "release.allow_dirty_worktree", "patch"
            )
        )
    if "changelog.body" in flat:
        config = config.replace(
            changelog_body=_require_str(flat["changelog.body"], "changelog.body", "patch")
        )
    if "changelog.footer" in flat:
        config = config.replace(
            changelog_footer=_require_str(flat["changelog.footer"], "changelog.footer", "patch")
        )
    if "git.max_commits" in flat:
        config = config.replace(
            git_max_commits=_require_int(flat["git.max_commits"], "git.max_commits", "patch")
        )
    return config


