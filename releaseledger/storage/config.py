def project_name_or_default(project) -> str:
    """Return the project name, falling back to the tool or directory name.

    ``project`` is a :class:`ReleaseledgerProject` or any object with
    ``layout.project_name`` and ``project_root``. The project name now
    comes from the canonical Ledger manifest; the function still
    returns a sensible string when it is missing so the CLI can render
    a human label.
    """

    layout = getattr(project, "layout", None)
    if layout is not None:
        manifest_name = getattr(layout, "project_name", None)
        if isinstance(manifest_name, str) and manifest_name.strip():
            return manifest_name
    root = getattr(project, "project_root", None)
    if root is not None:
        return Path(str(root)).name or DEFAULT_LEDGER_NAME
    return DEFAULT_LEDGER_NAME


def load_project_config(config_path: Path) -> ProjectConfig:
    """Load and validate the project TOML at ``config_path`` (version 2)."""

    if not config_path.is_file():
        raise LaunchError(
            f"Project config not found: {config_path}",
            code=CODE_CONFIG_ERROR,
            exit_code=2,
            data={"path": str(config_path)},
            remediation=[
                "Run `releaseledger init` to create the config.",
            ],
        )
    try:
        text = config_path.read_text(encoding="utf-8")
        document = tomlkit.parse(text)
    except tomlkit.exceptions.TomlKitError as exc:
        raise LaunchError(
            f"Failed to parse {config_path.name}: {exc}",
            code=CODE_CONFIG_ERROR,
            exit_code=2,
            data={"path": str(config_path)},
        ) from exc
    return _config_from_dict(document, config_path.name)


